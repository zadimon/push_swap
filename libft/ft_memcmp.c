#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t			i;
	int				res;
	unsigned char	*cs1;
	unsigned char	*cs2;

	i = 0;
	cs1 = (unsigned char *)s1;
	cs2 = (unsigned char *)s2;
	while (i < n)
	{
		if ((cs1[i] != cs2[i]))
		{
			res = cs1[i] - cs2[i];
			return (res);
		}
		i++;
	}
	return (0);
}
